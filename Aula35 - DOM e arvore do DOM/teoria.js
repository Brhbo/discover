/*
O Modelo de Objeto de Documentos (do inglês Document Object Model, DOM) é uma 
API definida pelo W3C para representar e interagir com qualquer documento HTML
ou XML.
O DOM é um modelo de documento carregado pelo navegador. Este documento é 
representado através de uma árvore de nós, onde cada um destes nós 
representa uma parte do documento (por ex. um elemento, texto ou comentário).
O DOM é uma das APIs mais usadas na Web (en-US) porque ele permite que cada
código rodando no navegador acesse e interaja com cada nó do documento. Os 
nós podem ser criados, movidos ou modificados. Listeners de evento podem também 
ser adicionados aos nós para serem disparados quando um dado evento ocorrer.
*/